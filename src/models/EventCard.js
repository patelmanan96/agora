function EventCard(img, title, tag, summary, desc, othertags, date, location) {
    this.image = img;
    this.title = title;
    this.tag = tag;
    this.summary = summary;
    this.description = desc;
    this.date = date;
    this.location = location;
    this.othertags = othertags;
}
export default EventCard;